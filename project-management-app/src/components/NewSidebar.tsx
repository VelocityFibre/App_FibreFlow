export default function Sidebar() {
  const pathname = usePathname();
  const [expandedItems, setExpandedItems] = useState<Record<string, boolean>>({ projects: true });

  const toggleExpand = (id: string) => {
    setExpandedItems(prev => ({
      ...prev,
      [id]: !prev[id]
    }));
  };

  const isActive = (href: string) => {
    return pathname === href;
  };

  const hasActiveChild = (item: MenuItem) => {
    if (!item.subItems) return false;
    return item.subItems.some(subItem => isActive(subItem.href || ''));
  };

  return (
    <div className="w-64 bg-[#003049] text-white min-h-screen p-4">
      <div className="text-xl font-bold mb-6 flex items-center">
        <svg xmlns="http://www.w3.org/2000/svg" className="h-6 w-6 mr-2" fill="none" viewBox="0 0 24 24" stroke="currentColor">
          <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M13 10V3L4 14h7v7l9-11h-7z" />
        </svg>
        FibreFlow
      </div>
      <nav>
        <ul className="space-y-2">
          {menuItems.map((item) => (
            <li key={item.id}>
              {item.subItems ? (
                <div>
                  <button
                    onClick={() => toggleExpand(item.id)}
                    className={`flex items-center w-full p-2 rounded hover:bg-[#004b74] ${hasActiveChild(item) ? 'bg-[#004b74]' : ''}`}
                  >
                    {item.icon && <span className="mr-2">{item.icon}</span>}
                    <span>{item.label}</span>
                    <span className="ml-auto">
                      {expandedItems[item.id] ? (
                        <svg xmlns="http://www.w3.org/2000/svg" className="h-4 w-4" fill="none" viewBox="0 0 24 24" stroke="currentColor">
                          <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M19 9l-7 7-7-7" />
                        </svg>
                      ) : (
                        <svg xmlns="http://www.w3.org/2000/svg" className="h-4 w-4" fill="none" viewBox="0 0 24 24" stroke="currentColor">
                          <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M9 5l7 7-7 7" />
                        </svg>
                      )}
                    </span>
                  </button>
                  {expandedItems[item.id] && (
                    <ul className="pl-4 mt-1 space-y-1">
                      {item.subItems.map((subItem) => (
                        <li key={subItem.id}>
                          <Link 
                            href={subItem.href || '#'}
                            className={`block p-2 rounded ${isActive(subItem.href || '') ? 'bg-[#0077b6]' : 'hover:bg-[#004b74]'}`}
                          >
                            {subItem.label}
                          </Link>
                        </li>
                      ))}
                    </ul>
                  )}
                </div>
              ) : (
                <Link 
                  href={item.href || '#'}
                  className={`flex items-center p-2 rounded ${isActive(item.href || '') ? 'bg-[#0077b6]' : 'hover:bg-[#004b74]'}`}
                >
                  {item.icon && <span className="mr-2">{item.icon}</span>}
                  <span>{item.label}</span>
                </Link>
              )}
            </li>
          ))}
        </ul>
      </nav>
    </div>
  );
}
